#include <assert.h>
#include "DIBSection.hpp"



////////////////////////////////////////////////////////////////////////////////

wDIBSection::wDIBSection(int width, int height)
{
  if (width == 0 || height == 0)
  {
    m_Image = (wxImage *)NULL;
    return;
  }

  m_Image = new wxImage(width, height);

}

////////////////////////////////////////////////////////////////////////////////

wDIBSection::~wDIBSection()
{
  if (m_Image != (wxImage *) NULL) {
    m_Image.Destroy();
  }
}

