#include "LogFile.hpp"
////////////////////////////////////////////////////////////////////////////////
CLogFile::CLogFile(const char* filename)
{
    m_File = fopen(filename, "w");
}
////////////////////////////////////////////////////////////////////////////////
CLogFile::~CLogFile()
{
    fclose(m_File);
}
////////////////////////////////////////////////////////////////////////////////
void
CLogFile::WriteString(const char* string)
{
    if (m_File != NULL)
    {
        fputs(string, m_File);
        fflush(m_File);
    }
}
